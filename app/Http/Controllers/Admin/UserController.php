     *
     * @return \Illuminate\Http\Response
     */
    public function sendMail(Request $request)
    {
        $validatedData = $request->validate([
            'checkbox_mail' => 'required',
            'body_message' => 'required'
        ]);

        $items = $request->checkbox_mail;

        foreach ($items as $id) {
            $customer = User::where('id',$id)->first();

            $customer_email = $customer->email;

            Mail::to($customer_email)->send(new SendMail());
        }

        return redirect()->route('users.index')->with('message_alert','Mail successfully sent!');
    }
